public class PrintUtil {

	/**
	 * 格式化打印
	 * @author macrotea@qq.com
	 * @date 2012-12-29 上午10:19:57
	 * @param text
	 */
	public static void formatPrint(String text) {
		System.out.println("\n####################################################");
		System.out.println("\t" + text);
		System.out.println("####################################################");
	}

}
